package exercicio;

public class ExercicioPooConverterMoeda {
    public double cotacaoDollar;
    public double dollar;
    public double IOF;
    public double reais;

    public double converterDollar(){
        return reais = dollar * cotacaoDollar;
    }
    public double calcIOF(){
        return IOF = converterDollar() * 0.06;
    }
    public double total(){
        return converterDollar() + calcIOF();
    }

}
